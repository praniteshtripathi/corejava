package rnd.program;

public class LocationProp {
	String name;
	String nttTriggerStateKey;
	String nttTriggerStateValue;
	String nttTriggerGroupKey;
	String nttTriggerGroupValue;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNttTriggerStateKey() {
		return nttTriggerStateKey;
	}
	public void setNttTriggerStateKey(String nttTriggerStateKey) {
		this.nttTriggerStateKey = nttTriggerStateKey;
	}
	public String getNttTriggerStateValue() {
		return nttTriggerStateValue;
	}
	public void setNttTriggerStateValue(String nttTriggerStateValue) {
		this.nttTriggerStateValue = nttTriggerStateValue;
	}
	public String getNttTriggerGroupKey() {
		return nttTriggerGroupKey;
	}
	public void setNttTriggerGroupKey(String nttTriggerGroupKey) {
		this.nttTriggerGroupKey = nttTriggerGroupKey;
	}
	public String getNttTriggerGroupValue() {
		return nttTriggerGroupValue;
	}
	public void setNttTriggerGroupValue(String nttTriggerGroupValue) {
		this.nttTriggerGroupValue = nttTriggerGroupValue;
	}
	
}
